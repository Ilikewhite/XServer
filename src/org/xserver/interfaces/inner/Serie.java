package org.xserver.interfaces.inner;

import java.util.List;

public class Serie {
	private String name;
	private List<Object> datas;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public List<Object> getDatas() {
		return datas;
	}

	public void setDatas(List<Object> datas) {
		this.datas = datas;
	}

}
